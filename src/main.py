def add_numbers(a, b):
    sum = a + b
    return sum;

def divide_numbers(dividend, divisor):
    quotient = dividend/divisor
    return quotient

def multiply_numbers(a, b):
    product = a * b
    return product

def subtract_numbers(minuend, subtrahend): # minuend = starting number, subtrahend = number you are subracting from minuend
    difference = minuend - subtrahend
    return difference
